def checkExperienceAndLevelUp(character):
    experienceNeededToLevelUp = experienceTable[character.level]
    if character.experience >= experienceNeededToLevelUp:
        character.level += 1
        print("{} has leveled up!".format(character.name))
        time.sleep(3)
        increaseCharacterStats(character)


def increaseCharacterStats(character):
    if isinstance(character, FFBattleSystem.Fighter):
        characterStatsDict = fighterStats
    elif isinstance(character, FFBattleSystem.Thief):
        characterStatsDict = thiefStats
    elif isinstance(character, FFBattleSystem.Monk):
        characterStatsDict = monkStats
    elif isinstance(character, FFBattleSystem.RedMage):
        characterStatsDict = redMageStats
    elif isinstance(character, FFBattleSystem.WhiteMage):
        characterStatsDict = whiteMageStats
    else:
        characterStatsDict = blackMageStats

    if characterStatsDict['MaxHP'][character.level]:
        character.maxHP += characterStatsDict['MaxHP'][character.level]
        character.currentHP += characterStatsDict['MaxHP'][character.level]
        print("--HP increases by {}.".format(characterStatsDict['MaxHP'][character.level]))
        time.sleep(0.5)
    if characterStatsDict['Str'][character.level]:
        character.strength += characterStatsDict['Str'][character.level]
        print("--Strength increases by {}.".format(characterStatsDict['Str'][character.level]))
        time.sleep(0.5)
    if characterStatsDict['Armor'][character.level]:
        character.armor += characterStatsDict['Armor'][character.level]
        print("--Defense increases by {}.".format(characterStatsDict['Armor'][character.level]))
        time.sleep(0.5)
    if characterStatsDict['Agi'][character.level]:
        character.agility += characterStatsDict['Agi'][character.level]
        print("--Agility increases by {}.".format(characterStatsDict['Agi'][character.level]))
        time.sleep(0.5)
    if characterStatsDict['Int'][character.level]:
        character.intelligence += characterStatsDict['Int'][character.level]
        print("--Intelligence increases by {}.".format(characterStatsDict['Int'][character.level]))
        time.sleep(0.5)
    if characterStatsDict['Vit'][character.level]:
        character.vitality += characterStatsDict['Vit'][character.level]
        print("--Vitality increases by {}.".format(characterStatsDict['Vit'][character.level]))
        time.sleep(0.5)
    if characterStatsDict['Luck'][character.level]:
        character.luck += characterStatsDict['Luck'][character.level]
        print("--Luck increases by {}.".format(characterStatsDict['Luck'][character.level]))
        time.sleep(0.5)
    if characterStatsDict['Hit'][character.level]:
        character.hitRate += characterStatsDict['Hit'][character.level]
        print("--Strike count increases by {}.".format(characterStatsDict['Hit'][character.level]))
        time.sleep(0.5)
    if characterStatsDict['MDef'][character.level]:
        character.magicDef += characterStatsDict['MDef'][character.level]
        print("--Magic defense increases by {}.".format(characterStatsDict['MDef'][character.level]))
        time.sleep(0.5)
    if character.level in characterStatsDict['Spell'].keys():
        character.spellsKnown[characterStatsDict['Spell'][character.level]] = [0, 1]
        print("--{} has learned '{}'!".format(character.name, characterStatsDict['Spell'][character.level].name))
    time.sleep(3)


